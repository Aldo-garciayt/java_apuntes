//Declar dos variables numericas y mostrar el reslutados entre ellas de una suma,resta,multiplicacion, division y modulo
package ejerciciope1;
public class EjercicioPE1 {

    /*//
     */
    public static void main(String[] args) {
        int n1=10;
        int n2=20;
        
        System.out.println("La suma de los dos numeros es:" + (n1+n2));
        System.out.println("La resta de los dos numeros es:" + (n1-n2));
        System.out.println("La multiplicacion de los dos numeros es:" + (n1*n2));
        System.out.println("La division de los dos numeros es:" + (n1/n2));
        System.out.println("El modulo de los dos numeros es:" + (n1%n2));
        
    }
    
}
